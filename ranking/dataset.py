"""
ranking/dataset.py
-------------------
PyTorch Dataset for the ranking model (NeuMF / DeepFM).

Each sample is one (user, item, label) triple, augmented with:
  - Pre-computed user feature vector  (from user_features.npy)
  - Pre-computed item feature vector  (from item_features.npy)

Positive samples come from the train interaction matrix.
Negative samples come from neg_samples_train.npy (generated by
preprocessing/negative_sampler.py).
"""

import os
import numpy as np
import torch
from torch.utils.data import Dataset
import scipy.sparse as sp

PROCESSED_DIR = os.path.join("data", "processed")


class RankingDataset(Dataset):
    """
    Parameters
    ----------
    split : 'train' | 'val' | 'test'
        Which interaction split to use for positives.
    use_features : bool
        If True, load and return pre-computed user/item feature vectors.
    neg_samples_path : str | None
        Path to pre-sampled negative array.  Only used for 'train'.
        If None for train, negatives are sampled on-the-fly (slower).
    k_neg : int
        On-the-fly negatives per positive (used when neg_samples_path is None).
    """

    def __init__(
        self,
        split: str = "train",
        use_features: bool = True,
        neg_samples_path: str = None,
        k_neg: int = 4,
        processed_dir: str = PROCESSED_DIR,
    ):
        self.split        = split
        self.use_features = use_features
        self.processed_dir = processed_dir

        # Load interaction matrix
        mat = sp.load_npz(os.path.join(processed_dir, f"{split}_interactions.npz"))
        self.n_users = mat.shape[0]
        self.n_items = mat.shape[1]

        # Positive pairs
        rows, cols = mat.nonzero()
        pos_users = rows.astype(np.int64)
        pos_items = cols.astype(np.int64)
        pos_labels = np.ones(len(pos_users), dtype=np.float32)

        # Negative pairs
        if split == "train":
            if neg_samples_path is None:
                neg_samples_path = os.path.join(processed_dir, "neg_samples_train.npy")

            if os.path.exists(neg_samples_path):
                neg_arr = np.load(neg_samples_path)
                neg_users = neg_arr[:, 0].astype(np.int64)
                neg_items = neg_arr[:, 1].astype(np.int64)
            else:
                # Fast on-the-fly fallback (uniform negative sampling)
                print("  [RankingDataset] neg_samples_train.npy not found — "
                      "sampling on the fly (uniform, slower).")
                rng = np.random.default_rng(42)
                n_negs = len(pos_users) * k_neg
                neg_users = rng.choice(self.n_users, n_negs).astype(np.int64)
                neg_items = rng.choice(self.n_items, n_negs).astype(np.int64)

            neg_labels = np.zeros(len(neg_users), dtype=np.float32)

            self.users  = np.concatenate([pos_users, neg_users])
            self.items  = np.concatenate([pos_items, neg_items])
            self.labels = np.concatenate([pos_labels, neg_labels])
        else:
            # Val / test: only positives (evaluated against all items)
            self.users  = pos_users
            self.items  = pos_items
            self.labels = pos_labels

        # Shuffle training data
        if split == "train":
            perm = np.random.default_rng(0).permutation(len(self.users))
            self.users  = self.users[perm]
            self.items  = self.items[perm]
            self.labels = self.labels[perm]

        # Load feature matrices if requested
        self.user_features = None
        self.item_features = None
        if use_features:
            uf_path = os.path.join(processed_dir, "user_features.npy")
            if_path = os.path.join(processed_dir, "item_features.npy")
            if os.path.exists(uf_path) and os.path.exists(if_path):
                self.user_features = np.load(uf_path)  # (n_users, F_u)
                self.item_features = np.load(if_path)  # (n_items, F_i)
            else:
                print("  [RankingDataset] Feature files not found; "
                      "running without side features.")

        # Compute XGBoost repay scores for all pairs (one-time, at init)
        self.repay_scores = None
        xgb_path = os.path.join("models", "saved", "xgboost_repay.json")
        if (use_features and os.path.exists(xgb_path)
                and self.user_features is not None
                and self.item_features is not None):
            import xgboost as xgb
            _repay_model = xgb.Booster()
            _repay_model.load_model(xgb_path)
            X = np.hstack([
                self.user_features[self.users],
                self.item_features[self.items],
            ])
            self.repay_scores = _repay_model.predict(
                xgb.DMatrix(X)
            ).astype(np.float32)
            print(f"  [RankingDataset] Computed XGBoost repay scores  "
                  f"mean={self.repay_scores.mean():.3f}")

        print(f"  [RankingDataset] {split}: {len(self)} samples  "
              f"(pos={len(pos_users):,}, neg={len(self)-len(pos_users):,})")

    def __len__(self) -> int:
        return len(self.users)

    def __getitem__(self, idx: int):
        user_idx  = torch.tensor(self.users[idx],  dtype=torch.long)
        item_idx  = torch.tensor(self.items[idx],  dtype=torch.long)
        label     = torch.tensor(self.labels[idx], dtype=torch.float32)

        sample = {"user_idx": user_idx, "item_idx": item_idx, "label": label}

        if self.user_features is not None:
            sample["user_feats"] = torch.tensor(
                self.user_features[self.users[idx]], dtype=torch.float32)
        if self.item_features is not None:
            sample["item_feats"] = torch.tensor(
                self.item_features[self.items[idx]], dtype=torch.float32)
        if self.repay_scores is not None:
            sample["repay_score"] = torch.tensor(
                [self.repay_scores[idx]], dtype=torch.float32)  # shape (1,)

        return sample
